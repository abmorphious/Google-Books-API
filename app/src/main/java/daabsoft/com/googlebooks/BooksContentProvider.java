package daabsoft.com.googlebooks;

import android.content.ContentProvider;
import android.content.ContentValues;
import android.database.Cursor;
import android.database.sqlite.SQLiteDatabase;
import android.net.Uri;

import androidx.annotation.NonNull;
import androidx.annotation.Nullable;

public class BooksContentProvider extends ContentProvider {
    private BookDbOpenHelper bookDbOpenHelper;

    @Override
    public boolean onCreate() {
        bookDbOpenHelper = new BookDbOpenHelper(getContext());
        return true;
    }

    @Override
    public Cursor query(Uri uri, String[] projection, String selection,
                        String[] selectionArgs, String sortOrder) {
        Cursor cursor = null;
        SQLiteDatabase readableDatabase = bookDbOpenHelper.getReadableDatabase();
        cursor = readableDatabase.query(BookDbContract.TABLE_NAME, projection, selection, null
                , null, null, BookDbContract.BookEntry.BOOK_TITLE);
        return cursor;
    }

    @Nullable
    @Override
    public String getType(@NonNull Uri uri) {
        return null;
    }

    @Nullable
    @Override
    public Uri insert(@NonNull Uri uri, @Nullable ContentValues contentValues) {
        return null;
    }

    @Override
    public int delete(@NonNull Uri uri, @Nullable String s, @Nullable String[] strings) {
        return 0;
    }

    @Override
    public int update(@NonNull Uri uri, @Nullable ContentValues contentValues, @Nullable String s, @Nullable String[] strings) {
        return 0;
    }

}
